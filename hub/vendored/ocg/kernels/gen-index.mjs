#!/usr/bin/env node
/**
 * gen-index.mjs — codegen the kernel registry (index.mjs) FROM the *.kernel.mjs
 * files on disk. Makes the "forgot to register the kernel in index.mjs" omission
 * structurally impossible (the Wave 26 / NIS2 red-CI gap: 174 nodes, 168 kernels).
 *
 * The Worker bundles statically (esbuild/wrangler — no runtime filesystem glob),
 * so index.mjs MUST stay explicit `import` statements. This script generates that
 * explicit/static registry from the filesystem so it can never drift behind the
 * kernel files, and keeps it human-readable for debugging.
 *
 * Modes:
 *   node gen-index.mjs --write   → (re)write index.mjs from the kernel files.
 *   node gen-index.mjs --check   → regenerate in memory, diff vs on-disk; exit 1 if stale.
 *   node gen-index.mjs           → defaults to --check (safe default for CI).
 *
 * Exports preserved verbatim: `KERNELS` (the map) and `getKernel(tool_id)`.
 * Backstop: scripts/check-kernel-coverage.mjs (a chaingraph NODE with no kernel
 * FILE at all) — complementary; this gate only guarantees every kernel FILE is
 * registered. Re-vendor to the worker after --write: cd mcp-apps-poc && node generate.mjs.
 * CONTRACT.md §A4: drop a kernel file → gen-index.mjs --write; never hand-edit index.mjs.
 */
import { readdirSync, readFileSync, writeFileSync } from 'node:fs';
import { resolve, dirname } from 'node:path';
import { fileURLToPath } from 'node:url';

const HERE = dirname(fileURLToPath(import.meta.url));
const INDEX = resolve(HERE, 'index.mjs');

// Strict allowlist: ONLY `<tool_id>.kernel.mjs` (lowercase id, digits, hyphens).
// Excludes every helper/test/gate file (_hash, _proof, index, *.test.mjs, *check*,
// *lint*, *parity*, *coverage*, *syntax*, *smoke*, *regen*, *scaffold*, …) by
// construction — none of them end in `.kernel.mjs`.
const KERNEL_RE = /^([a-z0-9][a-z0-9-]*)\.kernel\.mjs$/;

// Alias = leading letters + first digit group of the tool_id (separators removed),
// digit-prefixed ids get a `t` prefix (a JS identifier can't start with a digit).
// art-02 → art02 · art-141 → art141 · 505 → t505 · rca-02 → rca02 · qfa-01 → qfa01.
function aliasFor(tool_id) {
  const m = tool_id.replace(/-/g, '').match(/^([a-z]*)(\d+)/i);
  if (!m) throw new Error(`gen-index: cannot derive alias for tool_id "${tool_id}" (no <letters><digits> prefix).`);
  return (m[1] || 't') + m[2];
}

function discover() {
  const tool_ids = readdirSync(HERE)
    .map((f) => {
      const m = f.match(KERNEL_RE);
      return m ? m[1] : null;
    })
    .filter(Boolean)
    .sort(); // sort by tool_id → stable diffs, drop historical insertion order
  return tool_ids;
}

function render(tool_ids) {
  // Derive aliases + assert global uniqueness (never silently overwrite).
  const aliasOf = new Map();
  const seen = new Map(); // alias → tool_id
  for (const id of tool_ids) {
    const a = aliasFor(id);
    if (seen.has(a)) {
      throw new Error(
        `gen-index: alias collision "${a}" — both "${seen.get(a)}" and "${id}" map to it. ` +
        `Disambiguate one tool_id (append a suffix) before regenerating; never silently overwrite.`
      );
    }
    seen.set(a, id);
    aliasOf.set(id, a);
  }

  const aliasW = Math.max(...tool_ids.map((id) => aliasOf.get(id).length));
  const keyW = Math.max(...tool_ids.map((id) => `'${id}':`.length));

  const imports = tool_ids
    .map((id) => `import * as ${aliasOf.get(id).padEnd(aliasW)} from './${id}.kernel.mjs';`)
    .join('\n');

  const entries = tool_ids
    .map((id) => `  ${`'${id}':`.padEnd(keyW)} ${aliasOf.get(id)},`)
    .join('\n');

  return (
    '// AUTO-GENERATED by gen-index.mjs — DO NOT EDIT BY HAND.\n' +
    '// Run: node chaingraph/kernels/gen-index.mjs --write\n' +
    '//\n' +
    '// Kernel registry — maps a ChainGraph node tool_id to its pure decision kernel.\n' +
    '// The Worker imports this and dispatches gpu:false nodes to compute server-side.\n' +
    '// generate.mjs (server repo) vendors repo/chaingraph/kernels/ into data/kernels/.\n' +
    '\n' +
    imports +
    '\n\n' +
    'export const KERNELS = {\n' +
    entries +
    '\n};\n\n' +
    'export function getKernel(tool_id) {\n' +
    '  return KERNELS[tool_id] ?? null;\n' +
    '}\n'
  );
}

const tool_ids = discover();
const next = render(tool_ids);
const write = process.argv.includes('--write');

if (write) {
  writeFileSync(INDEX, next); // LF, trailing newline; byte-stable (sorted, no timestamps)
  console.log(`gen-index --write: wrote index.mjs (${tool_ids.length} kernels).`);
  process.exit(0);
}

// --check (default): regenerate in memory, compare to on-disk.
let current = '';
try {
  current = readFileSync(INDEX, 'utf8');
} catch {
  console.error('gen-index --check FAIL: index.mjs is missing. Run: node chaingraph/kernels/gen-index.mjs --write');
  process.exit(1);
}
if (current !== next) {
  console.error(
    `gen-index --check FAIL: index.mjs is stale or hand-edited (${tool_ids.length} kernel files on disk). ` +
    'Run: node chaingraph/kernels/gen-index.mjs --write'
  );
  process.exit(1);
}
console.log(`gen-index --check: index.mjs current (${tool_ids.length} kernels).`);
process.exit(0);

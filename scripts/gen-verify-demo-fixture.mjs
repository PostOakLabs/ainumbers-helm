#!/usr/bin/env node
// Generates the Verify view's built-in tampered-fixture demo (HELM-U3, TILE-
// EXPLORER discipline: "a verifier never observed to reject isn't known to
// verify"). Builds ONE real signed evidence bundle via the shipped H7 hub
// modules (kernel-runner/bundle/checkpoint/journal — same code a live run
// produces), a byte-tampered sibling proven to fail, and the demo keypair's
// PUBLIC half — then writes the whole thing as a static JS module the browser
// can import with no fetch and no daemon. Re-run and commit whenever
// envelope.mjs/bundle.mjs's wire shape changes; the Verify view itself never
// runs this (it only ever reads the committed output).
import { writeFileSync } from "node:fs";
import { join, dirname } from "node:path";
import { fileURLToPath } from "node:url";
import { createHash, generateKeyPairSync } from "node:crypto";
import { ml_dsa44 } from "../hub/vendored/ocg/kernels/_proof.mjs";
import { openJournal, appendEntry } from "../hub/journal.mjs";
import { buildCheckpoint } from "../hub/checkpoint.mjs";
import { assembleBundle } from "../hub/bundle.mjs";

const HERE = dirname(fileURLToPath(import.meta.url));
const OUT = join(HERE, "..", "ui", "fixtures", "verify-demo.mjs");

const keys = { ed25519: generateKeyPairSync("ed25519"), mldsa44: ml_dsa44.keygen() };

const db = openJournal(":memory:");
appendEntry(db, {
  streamId: "run-verify-demo-1",
  kind: "execution_state",
  runId: "run-verify-demo-1",
  entry: {
    period_start: "2026-07-23T00:00:00.000Z",
    period_end: "2026-07-23T00:00:01.000Z",
    reference_db_version: "demo-1",
    triggering_input_digest: `sha256:${"3".repeat(64)}`,
    humans_involved: [],
    step_id: "nodes:n1",
    state: "completed",
  },
});
const checkpoint = buildCheckpoint(db, { checkpointSeq: 1, keys, anchors: [] });

const bundle = assembleBundle({
  bundleId: "bundle-verify-demo-1",
  runId: "run-verify-demo-1",
  workflowManifestDigest: `sha256:${"0".repeat(64)}`,
  specs: [
    {
      kind: "connector_attestation",
      subject: [{ name: "payload", digest: { sha256: createHash("sha256").update("demo").digest("hex") } }],
      predicate: {
        run_id: "run-verify-demo-1",
        connector_id: "google-drive.fetch",
        connector_version: "1.0.0",
        contract_digest: `sha256:${"1".repeat(64)}`,
        operation: "drive.files.get",
        scope: ["drive.file"],
        endpoint_host: "www.googleapis.com",
        payload_digest: `sha256:${createHash("sha256").update("demo").digest("hex")}`,
        requested_at: "2026-07-23T00:00:00.000Z",
      },
    },
    {
      kind: "execution_state",
      subject: [{ name: "output", digest: { sha256: "2".repeat(64) } }],
      predicate: { run_id: "run-verify-demo-1", step_id: "nodes:n1", state: "completed", output_digest: `sha256:${"2".repeat(64)}` },
    },
  ],
  checkpoints: [checkpoint],
  anchorsRef: [],
  keys,
});

const golden = bundle;
const tampered = structuredClone(bundle);
// Same tamper the H7 round-trip fixture proves against: corrupt one entry's
// signature bytes so it fails at the envelope, not the schema (a subtler,
// more convincing failure than corrupting JSON shape).
tampered.objects[0].envelope.signatures[0].sig = Buffer.from("not a real signature").toString("base64");

const publicKeys = {
  ed25519SpkiB64: keys.ed25519.publicKey.export({ format: "der", type: "spki" }).toString("base64"),
  mldsa44B64: Buffer.from(keys.mldsa44.publicKey).toString("base64"),
};

const contents = `// GENERATED by scripts/gen-verify-demo-fixture.mjs — DO NOT hand-edit.
// Real DSSE-signed evidence bundle (golden) + a byte-tampered sibling proven to
// fail verification, for the Verify view's built-in demo (HELM-U3).
export const DEMO_PUBLIC_KEYS = ${JSON.stringify(publicKeys, null, 2)};
export const DEMO_GOLDEN_BUNDLE = ${JSON.stringify(golden, null, 2)};
export const DEMO_TAMPERED_BUNDLE = ${JSON.stringify(tampered, null, 2)};
`;

writeFileSync(OUT, contents);
console.log(`Wrote ${OUT}`);
db.close();

#!/usr/bin/env node
// Compiles the Verify view's ESM crypto/schema chain (ui/vendored/{hash,proof,
// der,schema-validator}.mjs + ui/vendored/schemas/evidence_bundle_manifest.schema.mjs
// + ui/lib/{verify-envelope,verify-bundle}.mjs) into ONE dependency-free script
// string, for HELM-P3-V9's offline single-file bundle verifier — a colleague
// opens verify.html from an emailed bundle.zip with no server, no import
// resolution, no build step (D2), so the seven ES modules above are inlined
// into a small hand-rolled module system (`NS.<id> = (function(){...})();`,
// each module keeping its own closure so internal helpers of the same name in
// different files — e.g. der.mjs's and verify-envelope.mjs's own local
// base64ToBytes — never collide) instead of a generic bundler dependency.
// Re-run and commit whenever any of the seven source files' import/export
// surface changes; nothing at runtime ever executes this script.
import { readFileSync, writeFileSync } from "node:fs";
import { join, dirname } from "node:path";
import { fileURLToPath } from "node:url";

const HERE = dirname(fileURLToPath(import.meta.url));
const ROOT = join(HERE, "..");
const OUT = join(ROOT, "ui", "lib", "standalone-verifier-runtime.gen.mjs");

// Compile order = dependency order. `imports` maps this module's import
// specifiers to the id of the module they resolve to (hand-mapped — only
// seven fixed files, not a general resolver).
const MODULES = [
  { id: "hash", file: "ui/vendored/hash.mjs", imports: {} },
  { id: "proof", file: "ui/vendored/proof.mjs", imports: { "./hash.mjs": "hash" } },
  { id: "der", file: "ui/vendored/der.mjs", imports: {} },
  { id: "schemaValidator", file: "ui/vendored/schema-validator.mjs", imports: {} },
  { id: "manifestSchema", file: "ui/vendored/schemas/evidence_bundle_manifest.schema.mjs", imports: {}, hasDefault: true },
  {
    id: "verifyEnvelope",
    file: "ui/lib/verify-envelope.mjs",
    imports: { "../vendored/hash.mjs": "hash", "../vendored/proof.mjs": "proof" },
  },
  {
    id: "verifyBundle",
    file: "ui/lib/verify-bundle.mjs",
    imports: {
      "./verify-envelope.mjs": "verifyEnvelope",
      "../vendored/der.mjs": "der",
      "../vendored/schema-validator.mjs": "schemaValidator",
      "../vendored/schemas/evidence_bundle_manifest.schema.mjs": "manifestSchema",
    },
  },
];

const IMPORT_LINE = /^import\s+(\{[^}]*\}|[A-Za-z0-9_$]+)\s+from\s+["']([^"']+)["'];?\s*$/gm;
const EXPORT_BLOCK = /^export\s*\{([^}]*)\}\s*;?\s*$/gm;
const EXPORT_DECL = /^export\s+(async function|function|class|const|let|var)\s+([A-Za-z0-9_$]+)/gm;

function compileModule({ id, file, imports, hasDefault }) {
  let body = readFileSync(join(ROOT, file), "utf8");
  const exported = new Set();

  body = body.replace(IMPORT_LINE, (m, clause, spec) => {
    const target = imports[spec];
    if (!target) throw new Error(`gen-standalone-verifier-runtime: unmapped import "${spec}" in ${file}`);
    if (clause.startsWith("{")) {
      const names = clause
        .slice(1, -1)
        .split(",")
        .map((s) => s.trim())
        .filter(Boolean)
        .map((s) => s.replace(/\s+as\s+/, ": "));
      return `const { ${names.join(", ")} } = NS.${target};`;
    }
    return `const ${clause} = NS.${target}.default;`;
  });

  body = body.replace(EXPORT_BLOCK, (m, names) => {
    names
      .split(",")
      .map((s) => s.trim())
      .filter(Boolean)
      .forEach((n) => {
        const [local, alias] = n.split(/\s+as\s+/).map((s) => s.trim());
        exported.add(alias ? `${alias}: ${local}` : local);
      });
    return "";
  });

  if (hasDefault) {
    let hit = false;
    body = body.replace(/export default\s*/, () => {
      hit = true;
      return "const __default__ = ";
    });
    if (!hit) throw new Error(`gen-standalone-verifier-runtime: ${file} expected an "export default" (hasDefault: true)`);
    exported.add("default: __default__");
  } else {
    body = body.replace(/export default\s+/g, ""); // vendored dead-code default (e.g. proof.mjs's noble _u64 internal) — discarded, unused cross-file
  }

  body = body.replace(EXPORT_DECL, (m, kind, name) => {
    exported.add(name);
    return `${kind} ${name}`;
  });

  if (/^export\s/m.test(body)) {
    throw new Error(`gen-standalone-verifier-runtime: unhandled "export" left in compiled ${file} — extend the compiler`);
  }

  return `NS.${id} = (function () {\n${body}\nreturn { ${[...exported].join(", ")} };\n})();\n`;
}

const compiled = ["const NS = {};", ...MODULES.map(compileModule)].join("\n");
const runtime = `${compiled}\nconst { verifyBundle, verifyAnchorBinding, assertRedacted, statementOf } = NS.verifyBundle;\n`;

const contents = `// GENERATED by scripts/gen-standalone-verifier-runtime.mjs — DO NOT hand-edit.
// One dependency-free script: ui/vendored/{hash,proof,der,schema-validator}.mjs
// + the evidence_bundle_manifest schema + ui/lib/{verify-envelope,verify-bundle}.mjs,
// inlined for HELM-P3-V9's offline single-file bundle verifier (embedded in
// every bundle.zip export — see ui/lib/standalone-verifier.mjs). Re-run this
// generator and commit whenever any source module's import/export surface
// changes; ui/lib/standalone-verifier.test.mjs proves this string still
// verifies a golden bundle and rejects a tampered one.
export const VERIFIER_RUNTIME_JS = ${JSON.stringify(runtime)};
`;

writeFileSync(OUT, contents);
console.log(`Wrote ${OUT} (${runtime.length} bytes of inlined runtime)`);

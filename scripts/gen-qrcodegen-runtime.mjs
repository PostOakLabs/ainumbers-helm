#!/usr/bin/env node
// Wraps the vendored ui/vendored/qrcodegen.js (verbatim, no imports/exports —
// see ui/vendored/PORT.md) as a JS string constant so ui/lib/auditor-pdf.mjs
// (HELM-P3-V9) can embed it in generated HTML with zero fs access — same
// discipline as gen-standalone-verifier-runtime.mjs. Re-run and commit
// whenever the vendored file is re-synced.
import { readFileSync, writeFileSync } from "node:fs";
import { join, dirname } from "node:path";
import { fileURLToPath } from "node:url";

const HERE = dirname(fileURLToPath(import.meta.url));
const ROOT = join(HERE, "..");
const SRC = join(ROOT, "ui", "vendored", "qrcodegen.js");
const OUT = join(ROOT, "ui", "lib", "qrcodegen-runtime.gen.mjs");

const source = readFileSync(SRC, "utf8");
const contents = `// GENERATED by scripts/gen-qrcodegen-runtime.mjs — DO NOT hand-edit.
// Verbatim ui/vendored/qrcodegen.js as a string constant (Project Nayuki, MIT —
// see ui/vendored/PORT.md), for HELM-P3-V9's auditor PDF QR code.
export const QRCODEGEN_JS = ${JSON.stringify(source)};
`;

writeFileSync(OUT, contents);
console.log(`Wrote ${OUT} (${source.length} bytes)`);
